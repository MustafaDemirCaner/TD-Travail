def majusculer(lettre):
    """retourne la lettre majuscule qui correspond à la minuscule <lettre>.
    :param lettre: (str) lettre doit contenir un seul caractère qui est une lettre minuscule.
    :return : (str) la majuscule correspondante."""
    return lettre.upper()

print(majusculer("a"))
print(majusculer("A"))